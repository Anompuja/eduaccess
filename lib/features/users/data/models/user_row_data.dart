class UserRowData {
  final String userId;
  final String name;
  final String email;
  final String role;
  final String phone;
  final String status;
  final String createdAt;
  final String updatedAt;

  const UserRowData({
    required this.userId,
    required this.name,
    required this.email,
    required this.role,
    required this.phone,
    required this.status,
    required this.createdAt,
    required this.updatedAt,
  });
}
