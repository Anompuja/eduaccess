class StaffRowData {
  final String staffId;
  final String name;
  final String role;
  final String email;
  final String phone;
  final String status;
  final String createdAt;
  final String updatedAt;

  const StaffRowData({
    required this.staffId,
    required this.name,
    required this.role,
    required this.email,
    required this.phone,
    required this.status,
    required this.createdAt,
    required this.updatedAt,
  });
}
